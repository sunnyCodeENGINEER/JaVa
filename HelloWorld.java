public class HelloWorld {
    public static void main(String[] args) {
        // implicit type conversion
        int number = 5;
        double number2 = number;
        double rep = 5.84;
        int number3 = (int) rep;
        
    
        System.out.println(number2);
        System.out.println(rep);
        System.out.println(number3);
    }
}
