public class RelationalOperators {
    public static void main(String[] args) {
        int number1 = 9;
        int number2 =71;

        // is equal to
        System.out.println(number1 == number2);

        // is not equal to
        System.out.println(number1 != number2);

        // is greater than
        System.out.println(number1 > number2);

        // is less than
        System.out.println(number1 < number2);

        // greater than or equal to
        System.out.println(number1 <= number2);

        // less than or equal to
        System.out.println(number1 >= number2);
    }
}
